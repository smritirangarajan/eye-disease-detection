cnv = r"""
**For CNV (Choroidal Neovascularization)**

- **Recommendation**
  - **Immediate Referral:** Seek prompt evaluation by a retinal specialist. CNV is often associated with conditions like AMD.
- **Treatment Options**
  - **Anti-VEGF Therapy:** Intravitreous injections (e.g., ranibizumab, aflibercept) are commonly used.
  - **Photodynamic Therapy (PDT):** Sometimes used alongside anti-VEGF.
  - **Laser Treatment:** Less common now, but may be recommended in specific situations.
- **Lifestyle & Monitoring**
  - **Diet:** Leafy greens, omega-3 fatty acids, and antioxidants (lutein, zeaxanthin).
  - **Supplements:** Consider AREDS2 formulation for AMD risk.
  - **Regular Monitoring:** Repeat OCT every 1–3 months to assess response.
- **Next Steps**
  - Schedule immediate follow-up with a retina specialist.
  - If on anti-VEGF, expect regular treatments initially, then taper as condition stabilizes.
"""

dme = r"""
**For DME (Diabetic Macular Edema)**

- **Recommendation**
  - **Endocrinology Consultation:** Coordinate care to optimize systemic control.
- **Treatment Options**
  - **Anti-VEGF Injections:** First-line to reduce swelling and improve vision.
  - **Corticosteroid Implants:** Consider if anti-VEGF is less effective.
  - **Laser Photocoagulation:** Focal/grid laser for areas of leakage (esp. non-center-involving DME).
- **Blood Sugar & Pressure Control**
  - **Glycemic Control:** Keep HbA1c < 7% if safe/appropriate.
  - **Blood Pressure:** Aim < 140/80 mmHg.
- **Monitoring**
  - **Frequent Eye Exams:** OCT every 3–6 months to track fluid and response.
  - **Diabetes Management:** Tight control helps prevent recurrences.
- **Next Steps**
  - Follow up with retina specialist and endocrinologist.
  - Continue anti-VEGF as needed; ensure systemic conditions are managed.
"""

drusen = r"""
**For Drusen (Early AMD)**

- **Dietary Changes**
  - **High in Antioxidants:** Vitamins C, E, zinc, copper, beta-carotene.
- **Supplements**
  - **AREDS2:** Recommended for moderate–high AMD risk.
- **Lifestyle Modifications**
  - **Quit Smoking:** Strongly reduces AMD progression risk.
  - **UV Protection:** Wear sunglasses to reduce retinal damage.
- **Monitoring**
  - **Regular OCT Scans:** Every 6–12 months to watch for changes.
  - **Amsler Grid:** Self-monitor for sudden vision changes.
- **Next Steps**
  - Discuss AREDS2 and lifestyle changes with your provider.
  - Keep routine OCT to monitor drusen size and AMD progression.
"""

